// Funciones en Javascript

// Declarando una función:
function saludar()
{
    let respuesta = prompt("¡Hola Santi! ¿Como fue tu día");

    if (respuesta == "bien")
    {
        alert("Me alegro");
    }
    else
    {
        alert("Una pena");
    }
}



// Llamando a la función:
saludar();




